            <p>Ce logiciel écrit en PHP repose sur une base de données MySQL et est distribué par la fondation WordPress.org. Les fonctionnalités de WordPress lui permettent de créer et gérer différents types de sites Web : site vitrine, site de vente en ligne, site applicatif, blog, portfolio, site institutionnel, site d'enseignement...

Il est distribué selon les termes de la licence GNU GPL version 2. Le logiciel est aussi utilisé comme socle du service multi-site WordPress.com3, celui-ci supporte plusieurs millions de sites4.

À la date du 1er décembre 2020 :

WordPress est utilisé par 39,1 % des sites web dans le monde,
ses concurrents directs open-source sont à 2,2 % (Joomla!) et à 1,6 % (Drupal)
38,7 % des sites n'utilisent pas de système de gestion de contenu5.</p>
            <p>WordPress trouve ses origines dans le logiciel b2 développé par Michel Valdrighi en 20016, un logiciel de publication de blog open-source reposant sur une base de données (MySQL). Michel Valdrighi ajoute de nombreuses fonctionnalités avant d'arrêter le développement du logiciel pour des raisons personnelles à la fin de 20027.

En janvier 2003, Matthew Mullenweg (PDG fondateur en 2005 de la startup d’édition de logiciel Automattic) et Mike Little corrigent les bogues de b2, ajoutent des fonctionnalités, et renomment le projet B2evolution, puis WordPress (sur la suggestion d'une amie de Matthew Mullenweg). Le 27 mai 2003, WordPress (version 0.7) devient officiellement le successeur du logiciel b2 et Michel Valdrighi réintègre l'équipe de développement8.

Depuis la version 1.0 sortie en janvier 2004, chaque version majeure de WordPress porte le nom de grandes vedettes du jazz. À la version 1.2 sortie en mai 2004 surnommée Mingus (pour Charles Mingus) succède la version 1.5 dite Strayhorn (pour Billy Strayhorn), qui atteint le cap de 150 000 téléchargements le 12 avril 2005, puis la version 2.0 publiée le 26 décembre 2005 et nommée Duke en hommage au pianiste et compositeur Duke Ellington. La version 2.1, nommée Ella (en référence à la chanteuse de jazz américaine Ella Fitzgerald), est publiée le 22 janvier 2007. Cette version est téléchargée plus de 1,5 million de fois (version anglaise).</p>
            <p>Quinze jours après la publication de la version 2.2, l'équipe de développement sort la version 2.3 de WordPress le 25 septembre 2007 sous le nom de Dexter (en l'honneur de Dexter Gordon).

Le 24 janvier 2008 doit apparaître la version 2.4 de WordPress, mais la sortie de celle-ci est annulée, les développeurs estiment que le délai ne peut pas être respecté. La sortie de la version 2.5, comprenant également les nouveautés initialement prévues dans la version 2.4, a lieu le 29 mars 2008 avec de grands changements notamment au niveau des templates et des extensions. Elle est nommée Brecker en référence aux Brecker Brothers (Michael Brecker saxophoniste et Randy Brecker trompettiste).

La version 2.6 est sortie le 15 juillet 2008 et est baptisée Tyner, en hommage au pianiste de jazz McCoy Tyner. Elle apporte de nombreuses nouveautés, dont la prévisualisation des thèmes, un système de révisions d'articles ainsi que la compatibilité avec le programme Google Gears, ou encore la possibilité de publier un article depuis n'importe quelle page sur Internet à l'aide d'un bouton inséré dans la barre d'outil du navigateur de l'utilisateur. Elle corrige 194 bogues issus des versions précédentes.

La version 2.7 sort le 11 décembre 2008, la plus grande amélioration se trouve dans la partie administrative, notamment grâce à l'ajout d'AJAX dans le panneau d'administration qui permet une plus grande flexibilité d'utilisation pour le blogueur.</p>
